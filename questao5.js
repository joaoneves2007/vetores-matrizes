/*
Crie uma matriz 3x3 e imprima o elemento na terceira linha e segunda
coluna em JavaScript?
*/

// Criando uma matriz 3x3
let matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

// Acessando o elemento na terceira linha e segunda coluna (índices começam em 0)
